package common.pojo;

public class DayUser {
	private int num;
	private int sd;
	private int fd;
	private int td;

	public DayUser(){
		this.num = 0;
		this.sd = 0;
		this.fd = 0;
		this.td = 0;
	}
	
	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getSd() {
		return sd;
	}

	public void setSd(int sd) {
		this.sd = sd;
	}

	public int getFd() {
		return fd;
	}

	public void setFd(int fd) {
		this.fd = fd;
	}

	public int getTd() {
		return td;
	}

	public void setTd(int td) {
		this.td = td;
	}

}
